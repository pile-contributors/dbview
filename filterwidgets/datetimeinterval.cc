/*  CLASS    --------------------------------------------------------------- */

/* ------------------------------------------------------------------------- */
DateTimeInterval::DateTimeInterval (QWidget *parent) :
    QWidget(parent),
    ui(new Ui::DateTimeInterval)
{
    ui->setupUi(this);
}
/* ========================================================================= */

/* ------------------------------------------------------------------------- */
DateTimeInterval::~DateTimeInterval()
{
    delete ui;
}
/* ========================================================================= */

/* ------------------------------------------------------------------------- */
QDateTime DateTimeInterval::startInterval() const
{
    return ui->startDateTimeEdit->dateTime ();
}
/* ========================================================================= */

/* ------------------------------------------------------------------------- */
QDateTime DateTimeInterval::endInterval() const
{
    return ui->endDateTimeEdit->dateTime ();
}
/* ========================================================================= */

/* ------------------------------------------------------------------------- */
bool DateTimeInterval::inclusive() const
{
    return ui->inclusiveCheckBox->checkState() == Qt::Checked;
}
/* ========================================================================= */

/* ------------------------------------------------------------------------- */
void DateTimeInterval::setStartInterval(const QDateTime &value)
{
    ui->startDateTimeEdit->setDateTime (value);
}
/* ========================================================================= */

/* ------------------------------------------------------------------------- */
void DateTimeInterval::setEndInterval(const QDateTime &value)
{
    ui->endDateTimeEdit->setDateTime (value);
}
/* ========================================================================= */

/* ------------------------------------------------------------------------- */
void DateTimeInterval::setinclusive(bool value)
{
    ui->inclusiveCheckBox->setChecked (value);
}
