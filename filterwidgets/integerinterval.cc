/*  CLASS    --------------------------------------------------------------- */

/* ------------------------------------------------------------------------- */
IntegerInterval::IntegerInterval (QWidget *parent) :
    QWidget(parent),
    ui(new Ui::IntegerInterval)
{
    ui->setupUi(this);
}
/* ========================================================================= */

/* ------------------------------------------------------------------------- */
IntegerInterval::~IntegerInterval()
{
    delete ui;
}
/* ========================================================================= */

/* ------------------------------------------------------------------------- */
int IntegerInterval::startInterval() const
{
    return ui->startIntegerEdit->value ();
}
/* ========================================================================= */

/* ------------------------------------------------------------------------- */
int IntegerInterval::endInterval() const
{
    return ui->endIntegerEdit->value ();
}
/* ========================================================================= */

/* ------------------------------------------------------------------------- */
bool IntegerInterval::inclusive() const
{
    return ui->inclusiveCheckBox->checkState() == Qt::Checked;
}
/* ========================================================================= */

/* ------------------------------------------------------------------------- */
void IntegerInterval::setStartInterval(int value)
{
    ui->startIntegerEdit->setValue (value);
}
/* ========================================================================= */

/* ------------------------------------------------------------------------- */
void IntegerInterval::setEndInterval(int value)
{
    ui->endIntegerEdit->setValue (value);
}
/* ========================================================================= */

/* ------------------------------------------------------------------------- */
void IntegerInterval::setinclusive(bool value)
{
    ui->inclusiveCheckBox->setChecked (value);
}
