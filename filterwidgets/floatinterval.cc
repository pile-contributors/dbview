/*  CLASS    --------------------------------------------------------------- */

/* ------------------------------------------------------------------------- */
FloatInterval::FloatInterval (QWidget *parent) :
    QWidget(parent),
    ui(new Ui::FloatInterval)
{
    ui->setupUi(this);
}
/* ========================================================================= */

/* ------------------------------------------------------------------------- */
FloatInterval::~FloatInterval()
{
    delete ui;
}
/* ========================================================================= */

/* ------------------------------------------------------------------------- */
double FloatInterval::startInterval() const
{
    return ui->startFloatEdit->value ();
}
/* ========================================================================= */

/* ------------------------------------------------------------------------- */
double FloatInterval::endInterval() const
{
    return ui->endFloatEdit->value ();
}
/* ========================================================================= */

/* ------------------------------------------------------------------------- */
bool FloatInterval::inclusive() const
{
    return ui->inclusiveCheckBox->checkState() == Qt::Checked;
}
/* ========================================================================= */

/* ------------------------------------------------------------------------- */
void FloatInterval::setStartInterval(double value)
{
    ui->startFloatEdit->setValue (value);
}
/* ========================================================================= */

/* ------------------------------------------------------------------------- */
void FloatInterval::setEndInterval(double value)
{
    ui->endFloatEdit->setValue (value);
}
/* ========================================================================= */

/* ------------------------------------------------------------------------- */
void FloatInterval::setInclusive(bool value)
{
    ui->inclusiveCheckBox->setChecked (value);
}
